package by.domain;

public class AnimeReportBean {

    private String anime_name;

    private String anime_date;

    private Integer anime_newSeries;

    public String getAnime_name() {
        return anime_name;
    }

    public void setAnime_name(String anime_name) {
        this.anime_name = anime_name;
    }

    public String getAnime_date() {
        return anime_date;
    }

    public void setAnime_date(String anime_date) {
        this.anime_date = anime_date;
    }

    public Integer getAnime_newSeries() {
        return anime_newSeries;
    }

    public void setAnime_newSeries(Integer anime_newSeries) {
        this.anime_newSeries = anime_newSeries;
    }
}
